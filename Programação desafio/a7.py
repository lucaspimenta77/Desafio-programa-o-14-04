# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

print('Iremos calcular a Area de seu quadrado e em seguida, mostrar o dobro edesta área')

p1 = float(input('Nos diga o comprimento de um lado do quadrado em cm: '))
q1 = p1**2 
q2 = q1 * 2
print(f'O tamanho da area de  seu quadrado é {q1}cm e o dobre de seu tamanho é {q2}cm')
